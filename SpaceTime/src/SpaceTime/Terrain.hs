initial_terrain :: Terrain
initial_terrain = read_terrain terrain_string

read_terrain :: [String] -> Terrain
read_terrain str_list = array (0, xbound) [ 
	(x, array (0, ybound) [ (y, value x y) | y<-[0..ybound] ] ) 
	| x<-[0..xbound] ] where
		value x y = (\c -> if c=='0' then True else False) (str_list !! (ybound-y) !! x )

